#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <time.h>
#include "sorting.h"

static int CountRuns(int n, int arr[n]) {
    if (n == 0) return 0;
    int runs = 1;
    for (int i = 1; i < n; i++)
        if (arr[i] < arr[i - 1]) runs++;
    return runs;
}

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    /* ==============================================================
     * Таблица 1: Трудоёмкость построения пирамиды (фаза 1)
     * N | M+C теор. | M+C факт убыв | M+C факт случ | M+C факт возр
     * ============================================================== */
    printf("\n┌──────┬────────────┬────────────┬────────────┬────────────┐\n");
    printf(  "│      │ HeapSort: Трудоёмкость построения пирамиды (фаза 1)  │\n");
    printf(  "│  N   ├────────────┬────────────┬────────────┬────────────┤\n");
    printf(  "│      │ M+C теор.  │ M+C(убыв)  │ M+C(случ)  │ M+C(возр)  │\n");
    printf(  "├──────┼────────────┼────────────┼────────────┼────────────┤\n");

    /* Данные считаем заранее для использования в остальных таблицах */
    int Mdec[5], Cdec[5], Mrnd[5], Crnd[5], Minc[5], Cinc[5];
    int theory[5];
    int runs_dec[5], runs_rnd[5], runs_inc[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int arr[n], orig[n];

        /* Фаза 1: ~2n */
        theory[s] = 2 * n;

        /* Фактические значения для всех трёх типов массива */
        FillDec(n, orig); CopyArray(n, orig, arr); HeapSort(n, arr, &Mdec[s], &Cdec[s]);
        runs_dec[s] = CountRuns(n, arr);

        FillRand(n, orig); CopyArray(n, orig, arr); HeapSort(n, arr, &Mrnd[s], &Crnd[s]);
        runs_rnd[s] = CountRuns(n, arr);

        FillInc(n, orig); CopyArray(n, orig, arr); HeapSort(n, arr, &Minc[s], &Cinc[s]);
        runs_inc[s] = CountRuns(n, arr);

        printf("│ %4d │ %10d │ %10d │ %10d │ %10d │\n",
            n, theory[s],
            Mdec[s]+Cdec[s], Mrnd[s]+Crnd[s], Minc[s]+Cinc[s]);
    }
    printf("└──────┴────────────┴────────────┴────────────┴────────────┘\n");

    /* ==============================================================
     * Таблица 2А: Полный HeapSort — теория вс факт (M+C)
     * N | Mt+Ct теор. | Mф+Cф(убыв) | Mф+Cф(случ) | Mф+Cф(возр)
     * Склеена с Таблицей 2Б: проверка через число серий
     * ============================================================== */
    {
        double lg_arr[5];
        int theory_full[5];
        for (int s = 0; s < 5; s++) {
            int n = sizes[s];
            double lg = log2((double)n);
            lg_arr[s] = lg;
            int Mt = (int)(n * lg + 6.5 * n - 4);
            int Ct = (int)(2.0 * n * lg + n + 2);
            theory_full[s] = Mt + Ct;
        }

        /* --- 2А: Теория вс факт M+C --- */
        printf("\n┌──────┬────────────┬────────────┬────────────┬────────────┐\n");
        printf(  "│      │ HeapSort: теоретическое вс фактическое (M+C)       │\n");
        printf(  "│  N   ├────────────┬────────────┬────────────┬────────────┤\n");
        printf(  "│      │ Mt+Ct теор. │ Mф+Cф(убыв) │ Mф+Cф(случ) │ Mф+Cф(возр) │\n");
        printf(  "├──────┼────────────┼────────────┼────────────┼────────────┤\n");
        for (int s = 0; s < 5; s++) {
            int n = sizes[s];
            printf("│ %4d │ %10d │ %10d │ %10d │ %10d │\n",
                n, theory_full[s],
                Mdec[s]+Cdec[s], Mrnd[s]+Crnd[s], Minc[s]+Cinc[s]);
        }
        printf("└──────┴────────────┴────────────┴────────────┴────────────┘\n");

        /* --- 2Б: Проверка правильности — число серий (должно быть 1) --- */
        printf("┌──────┬───────────┬───────────┬───────────┐\n");
        printf(  "│      │ Проверка после сортировки (серий = 1 — OK)│\n");
        printf(  "│  N   ├─────────┬─────────┬─────────┤\n");
        printf(  "│      │ Серий(убыв)│ Серий(случ)│ Серий(возр)│\n");
        printf(  "├──────┼─────────┼─────────┼─────────┤\n");
        for (int s = 0; s < 5; s++) {
            printf("│ %4d │ %7d │ %7d │ %7d │\n",
                sizes[s], runs_dec[s], runs_rnd[s], runs_inc[s]);
        }
        printf("└──────┴─────────┴─────────┴─────────┘\n");
    }

    /* ==============================================================
     * Таблица 3: Фактические M и C по отдельности
     * Разбита на 3 склеенных части по типу массива:
     * 3А = убывающий, 3Б = случайный, 3В = возрастающий
     * Каждая: N | M | C | M+C
     * ============================================================== */

    /* --- 3А: Убывающий --- */
    printf("\n┌──────┬─────────┬─────────┬──────────┐\n");
    printf(  "│      │ HeapSort: Факт. M и C — УБЫВАЮЩИЙ │\n");
    printf(  "│  N   ├───────┬───────┬────────┤\n");
    printf(  "│      │    M    │    C    │   M+C   │\n");
    printf(  "├──────┼───────┼───────┼────────┤\n");
    for (int s = 0; s < 5; s++)
        printf("│ %4d │ %5d │ %5d │ %6d │\n",
            sizes[s], Mdec[s], Cdec[s], Mdec[s]+Cdec[s]);
    printf("└──────┴───────┴───────┴────────┘\n");

    /* --- 3Б: Случайный --- */
    printf("┌──────┬─────────┬─────────┬──────────┐\n");
    printf(  "│      │ HeapSort: Факт. M и C — СЛУЧАЙНЫЙ  │\n");
    printf(  "│  N   ├───────┬───────┬────────┤\n");
    printf(  "│      │    M    │    C    │   M+C   │\n");
    printf(  "├──────┼───────┼───────┼────────┤\n");
    for (int s = 0; s < 5; s++)
        printf("│ %4d │ %5d │ %5d │ %6d │\n",
            sizes[s], Mrnd[s], Crnd[s], Mrnd[s]+Crnd[s]);
    printf("└──────┴───────┴───────┴────────┘\n");

    /* --- 3В: Возрастающий --- */
    printf("┌──────┬─────────┬─────────┬──────────┐\n");
    printf(  "│      │ HeapSort: Факт. M и C — ВОЗРАСТАЮЩИЙ│\n");
    printf(  "│  N   ├───────┬───────┬────────┤\n");
    printf(  "│      │    M    │    C    │   M+C   │\n");
    printf(  "├──────┼───────┼───────┼────────┤\n");
    for (int s = 0; s < 5; s++)
        printf("│ %4d │ %5d │ %5d │ %6d │\n",
            sizes[s], Minc[s], Cinc[s], Minc[s]+Cinc[s]);
    printf("└──────┴───────┴───────┴────────┘\n");

    return 0;
}
