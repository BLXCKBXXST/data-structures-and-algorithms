/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Метод прямого выбора с устранением фиктивных       ║
 * ║            перестановок (методичка 2.4 п.2)                  ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      DO (i = 1, 2, ... n-1)
 *          k := <номер наименьшего элемента из a_i, ... a_n>
 *          IF (k ≠ i)  a_i ↔ a_k  FI
 *      OD
 *  Обмен выполняется только если k ≠ i — это «устранение фиктивной перестановки».
 *  На отсортированном входе M = 0 (вместо 3(n-1)); C прежнее = (n² - n)/2.
 */
void SelectSortImproved(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    for (int i = 1; i <= n - 1; i++) {                  /* DO (i = 1..n-1) */
        int k = i;
        for (int j = i + 1; j <= n; j++) {
            (*C)++;
            if (arr[j-1] < arr[k-1]) k = j;
        }
        if (k != i) {                                    /* IF k ≠ i */
            int t = arr[i-1]; arr[i-1] = arr[k-1]; arr[k-1] = t;
            *M += 3;
        }
    }
}

