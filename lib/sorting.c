/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Метод Хоара / быстрая сортировка (методичка 4.2)   ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      Сортировка части массива с границами (L, R):
 *          x := a_L,  i := L,  j := R
 *          DO (i ≤ j)
 *              DO (a_i < x)  i := i+1  OD
 *              DO (a_j > x)  j := j-1  OD
 *              IF (i ≤ j)
 *                  a_i ↔ a_j,  i := i+1,  j := j-1
 *              FI
 *          OD
 *          IF (L < j)  <Сортировка (L, j)>  FI
 *          IF (i < R)  <Сортировка (i, R)>  FI
 *
 *  Ведущий элемент x = a_L (первый в рабочей части).
 *  Глубина рекурсии в худшем случае O(n), в среднем O(log n).
 *  Трудоёмкость средняя: M = O(n·log n), C = O(n·log n);
 *               худший:  M = 3(n-1), C = (n² + 5n + 4)/2.
 */
static void HoareSort(int arr[], int L, int R,
                      int *M, int *C, int d, int *maxd) {
    if (d > *maxd) *maxd = d;
    if (L >= R) return;
    int x = arr[L-1];                                     /* x := a_L */
    (*M)++;
    int i = L, j = R;
    while (i <= j) {                                       /* DO (i ≤ j) */
        while (1) {                                        /* DO (a_i < x) i := i+1 OD */
            (*C)++;
            if (arr[i-1] >= x) break;
            i = i + 1;
        }
        while (1) {                                        /* DO (a_j > x) j := j-1 OD */
            (*C)++;
            if (arr[j-1] <= x) break;
            j = j - 1;
        }
        if (i <= j) {                                      /* IF (i ≤ j) */
            int t = arr[i-1]; arr[i-1] = arr[j-1]; arr[j-1] = t;
            *M += 3;
            i = i + 1;
            j = j - 1;
        }
    }
    if (L < j) HoareSort(arr, L, j, M, C, d + 1, maxd);   /* IF (L < j) сорт(L,j) FI */
    if (i < R) HoareSort(arr, i, R, M, C, d + 1, maxd);   /* IF (i < R) сорт(i,R) FI */
}

void QuickSort1(int n, int arr[n], int *M, int *C, int *depth) {
    *M = 0; *C = 0; *depth = 0;
    if (n > 1) HoareSort(arr, 1, n, M, C, 1, depth);
}

