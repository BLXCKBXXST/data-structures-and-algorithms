/* ════════════════════════════════════════════════════
 *  Заполнение массивов и копирование (вспомогательные)
 * ════════════════════════════════════════════════════ */

void FillInc(int n, int arr[n]) {
    for (int i = 0; i < n; i++)
        arr[i] = i;
}

void FillDec(int n, int arr[n]) {
    for (int i = 0; i < n; i++)
        arr[i] = n - 1 - i;
}

void FillRand(int n, int arr[n]) {
    for (int i = 0; i < n; i++)
        arr[i] = rand() % (n * n);
}

void CopyArray(int n, int src[n], int dst[n]) {
    for (int i = 0; i < n; i++)
        dst[i] = src[i];
}

/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Метод прямого выбора (методичка 2.1)               ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      DO (i = 1, 2, ... n-1)
 *          k := <номер наименьшего элемента из a_i, ... a_n>
 *          a_i ↔ a_k
 *      OD
 *  Трудоёмкость: M = 3(n-1),  C = (n² - n)/2
 */
void SelectSort(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    for (int i = 1; i <= n - 1; i++) {                  /* DO (i = 1..n-1) */
        int k = i;                                       /* k := i      */
        for (int j = i + 1; j <= n; j++) {              /* поиск номера min из a_i..a_n */
            (*C)++;
            if (arr[j-1] < arr[k-1]) k = j;
        }
        /* a_i ↔ a_k */
        int t = arr[i-1]; arr[i-1] = arr[k-1]; arr[k-1] = t;
        *M += 3;
    }
}

