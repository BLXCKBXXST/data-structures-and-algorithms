/*  Алгоритм на псевдокоде:
 *      DO (i = 2, 3, ... n)
 *          t := a_i,  j := i-1
 *          DO (j > 0 и t < a_j)
 *              a_{j+1} := a_j
 *              j := j - 1
 *          OD
 *          a_{j+1} := t
 *      OD
 *  Трудоёмкость:
 *      C_min = n-1 (возрастающий),  C_max = (n²-n)/2 (убывающий)
 *      M_min = 2(n-1),              M_max = (n²-n)/2 + 2(n-1)
 */
void InsertSort(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    for (int i = 2; i <= n; i++) {                       /* DO (i = 2..n) */
        int t = arr[i-1];                                /* t := a_i */
        (*M)++;
        int j = i - 1;
        while (j > 0) {                                   /* DO (j > 0 и t < a_j) */
            (*C)++;
            if (t < arr[j-1]) {
                arr[j] = arr[j-1];                        /* a_{j+1} := a_j */
                (*M)++;
                j = j - 1;
            } else break;
        }
        arr[j] = t;                                       /* a_{j+1} := t */
        (*M)++;
    }
}

