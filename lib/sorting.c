/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Шейкерная сортировка (методичка 2.3)               ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      L := 1, R := n, k := n
 *      DO
 *          DO (j = R, R-1, ... L+1)
 *              IF (a_j < a_{j-1})  a_j ↔ a_{j-1}, k := j  FI
 *          OD
 *          L := k
 *          DO (j = L, L+1, ... R-1)
 *              IF (a_j > a_{j+1})  a_j ↔ a_{j+1}, k := j  FI
 *          OD
 *          R := k
 *      OD (L < R)
 *  k — индекс последнего обмена; границы рабочей части массива сдвигаются
 *  к нему. На отсортированном входе достаточно одного прохода (C = n-1).
 */
void ShakerSort(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    int L = 1, R = n, k = n;
    do {
        for (int j = R; j >= L + 1; j--) {              /* DO (j = R..L+1) */
            (*C)++;
            if (arr[j-1] < arr[j-2]) {                   /* IF (a_j < a_{j-1}) */
                int t = arr[j-1]; arr[j-1] = arr[j-2]; arr[j-2] = t;
                *M += 3;
                k = j;
            }
        }
        L = k;
        for (int j = L; j <= R - 1; j++) {              /* DO (j = L..R-1) */
            (*C)++;
            if (arr[j-1] > arr[j]) {                     /* IF (a_j > a_{j+1}) */
                int t = arr[j-1]; arr[j-1] = arr[j]; arr[j] = t;
                *M += 3;
                k = j;
            }
        }
        R = k;
    } while (L < R);                                     /* OD (L < R) */
}

