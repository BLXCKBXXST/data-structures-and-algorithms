/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Метод Хоара с уменьшением глубины рекурсии         ║
 * ║            (методичка 4.3)                                   ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      Сортировка части массива (L, R):
 *          DO (есть хотя бы 2 элемента, т.е. L < R)
 *              <разделение>  (как в 4.2)
 *              IF (j - L > R - i)         // левая часть длиннее правой
 *                  <Сортировка (i, R)>    // рекурсивно меньшую правую
 *                  R := j                 // итеративно левую
 *              ELSE
 *                  <Сортировка (L, j)>    // рекурсивно меньшую левую
 *                  L := i                 // итеративно правую
 *              FI
 *          OD
 *
 *  Идея: рекурсивно вызываем только для меньшей из двух частей, бо́льшую
 *  обрабатываем итеративно — глубина рекурсии не превосходит ⌊log₂ n⌋.
 */
static void HoareSortRedRec(int arr[], int L, int R,
                            int *M, int *C, int d, int *maxd) {
    if (d > *maxd) *maxd = d;
    while (L < R) {                                       /* DO (L < R) */
        /* <разделение> — как в 4.2 */
        int x = arr[L-1];                                 /* x := a_L */
        (*M)++;
        int i = L, j = R;
        while (i <= j) {
            while (1) { (*C)++; if (arr[i-1] >= x) break; i = i + 1; }
            while (1) { (*C)++; if (arr[j-1] <= x) break; j = j - 1; }
            if (i <= j) {
                int t = arr[i-1]; arr[i-1] = arr[j-1]; arr[j-1] = t;
                *M += 3;
                i = i + 1;
                j = j - 1;
            }
        }
        /* Рекурсивно для меньшей, итеративно для большей */
        if (j - L > R - i) {                              /* IF (j-L > R-i) */
            if (i < R) HoareSortRedRec(arr, i, R, M, C, d + 1, maxd);
            R = j;
        } else {
            if (L < j) HoareSortRedRec(arr, L, j, M, C, d + 1, maxd);
            L = i;
        }
    }
}

void QuickSort2(int n, int arr[n], int *M, int *C, int *depth) {
    *M = 0; *C = 0; *depth = 0;
    if (n > 1) HoareSortRedRec(arr, 1, n, M, C, 1, depth);
}

