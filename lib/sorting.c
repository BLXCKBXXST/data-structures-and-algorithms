/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Метод Шелла (методичка 3.2)                        ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      DO (k = h_m, h_{m-1}, ... 1)            // последовательность шагов
 *          DO (i = k+1, k+2, ... n)
 *              t := a_i,  j := i - k
 *              DO (j > 0 и t < a_j)
 *                  a_{j+k} := a_j
 *                  j := j - k
 *              OD
 *              a_{j+k} := t
 *          OD
 *      OD
 *  Последовательность Кнута: h_1 = 1, h_i = 2·h_{i-1}+1.
 *  Средняя трудоёмкость: M+C ≈ O(n^1.2).
 */
void ShellSort(int ns, int steps[ns], int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    for (int si = 0; si < ns; si++) {                    /* DO (k = h_m, h_{m-1}, ... 1) */
        int k = steps[si];
        for (int i = k + 1; i <= n; i++) {              /* DO (i = k+1..n) */
            int t = arr[i-1];                            /* t := a_i */
            (*M)++;
            int j = i - k;
            while (j > 0) {                              /* DO (j > 0 и t < a_j) */
                (*C)++;
                if (t < arr[j-1]) {
                    arr[(j-1) + k] = arr[j-1];            /* a_{j+k} := a_j */
                    (*M)++;
                    j = j - k;
                } else break;
            }
            arr[(j-1) + k] = t;                          /* a_{j+k} := t */
            (*M)++;
        }
    }
}

