/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Пузырьковая сортировка (методичка 2.2)             ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      DO (i = 1, 2, ... n-1)
 *          DO (j = n, n-1, ... i+1)
 *              IF (a_j < a_{j-1})  a_j ↔ a_{j-1}  FI
 *          OD
 *      OD
 *  Внутренний цикл идёт справа налево: лёгкие элементы «всплывают» к началу.
 *  Трудоёмкость: C = (n² - n)/2;
 *    M_min = 0 (на возрастающем),  M_max = 3(n² - n)/2 (на убывающем).
 */
void BubbleSort(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    for (int i = 1; i <= n - 1; i++) {                  /* DO (i = 1..n-1) */
        for (int j = n; j >= i + 1; j--) {              /* DO (j = n, n-1, ... i+1) */
            (*C)++;
            if (arr[j-1] < arr[j-2]) {                   /* IF (a_j < a_{j-1}) */
                /* a_j ↔ a_{j-1} */
                int t = arr[j-1]; arr[j-1] = arr[j-2]; arr[j-2] = t;
                *M += 3;
            }
        }
    }
}

