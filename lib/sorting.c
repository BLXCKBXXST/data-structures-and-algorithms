/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Пирамидальная сортировка (методичка 4.1)           ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *
 *  Построение (L,R)-пирамиды (вход: a_{L+1},...,a_R уже (L+1,R)-пирамида,
 *                              a_L — новый элемент)
 *      x := a_L,  i := L
 *      DO
 *          j := 2i
 *          IF (j > R)  OD
 *          IF ((j < R) и (a_{j+1} ≥ a_j))  j := j+1  FI
 *          IF (x ≥ a_j)  OD
 *          a_i := a_j
 *          i := j
 *      OD
 *      a_i := x
 *
 *  Главный алгоритм:
 *      L := ⌊n/2⌋
 *      DO (L > 0)
 *          <Построение (L,n)-пирамиды>
 *          L := L - 1
 *      OD
 *      R := n
 *      DO (R > 1)
 *          a_1 ↔ a_R
 *          R := R - 1
 *          <Построение (1,R)-пирамиды>
 *      OD
 *
 *  Используется max-пирамида (a_j ≥ max(a_{2j}, a_{2j+1})), чтобы получить
 *  результат в порядке возрастания. Методичка приводит вариант с min-пирамидой
 *  (для убывающего результата) — здесь зеркальный вариант.
 *
 *  Трудоёмкость: C ≤ 2n·log₂n + n + 2,  M ≤ n·log₂n + 6.5n - 4.
 */

/* Построение (L,R)-пирамиды: a_L просеивается вниз в (L,R)-диапазон. */
static void SiftLR(int arr[], int L, int R, int *M, int *C) {
    int x = arr[L-1];                                    /* x := a_L */
    (*M)++;
    int i = L;
    while (1) {                                           /* DO */
        int j = 2 * i;                                    /* j := 2i */
        if (j > R) break;                                 /* IF (j > R) OD */
        if (j < R) {                                      /* IF (j < R и a_{j+1} ≥ a_j) j := j+1 FI */
            (*C)++;
            if (arr[(j+1)-1] >= arr[j-1]) j = j + 1;
        }
        (*C)++;
        if (x >= arr[j-1]) break;                         /* IF (x ≥ a_j) OD */
        arr[i-1] = arr[j-1];                              /* a_i := a_j */
        (*M)++;
        i = j;
    }
    arr[i-1] = x;                                         /* a_i := x */
    (*M)++;
}

/* BuildHeap — построение пирамиды из произвольной последовательности.
 * По свойству (3): a_{⌊n/2⌋+1}, ..., a_n уже образуют (⌊n/2⌋+1, n)-пирамиду
 * (листы), достаточно добавлять слева, начиная с L = ⌊n/2⌋. */
void BuildHeap(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    int L = n / 2;                                        /* L := ⌊n/2⌋ */
    while (L > 0) {                                       /* DO (L > 0) */
        SiftLR(arr, L, n, M, C);                          /* <Построение (L,n)-пирамиды> */
        L = L - 1;
    }
}

void HeapSort(int n, int arr[n], int *M, int *C) {
    *M = 0; *C = 0;
    /* Фаза 1: построение (1,n)-пирамиды */
    int L = n / 2;                                        /* L := ⌊n/2⌋ */
    while (L > 0) {                                       /* DO (L > 0) */
        SiftLR(arr, L, n, M, C);
        L = L - 1;
    }
    /* Фаза 2: двустороннее усечение пирамиды → отсортированный массив */
    int R = n;
    while (R > 1) {                                       /* DO (R > 1) */
        int t = arr[0]; arr[0] = arr[R-1]; arr[R-1] = t;   /* a_1 ↔ a_R */
        *M += 3;
        R = R - 1;
        SiftLR(arr, 1, R, M, C);                          /* <Построение (1,R)-пирамиды> */
    }
}

