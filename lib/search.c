/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Поиск всех вхождений — расширение от найденного    ║
 * ║            (методичка 7.1, замечание к Версии 1)             ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Методичка: «Если в массиве несколько элементов с одинаковым ключом,
 *   то эта версия находит один из них. Чтобы найти все элементы с
 *   одинаковыми ключами, необходимо просмотреть массив влево и вправо
 *   от найденного элемента».
 *
 *  1) BSearch1 находит произвольное вхождение;
 *  2) расширяем влево и вправо линейно, пока соседи равны ключу.
 */
int BSearchAll1(int n, int arr[n], int X, int found[], int *C) {
    *C = 0;
    /* Найти любое вхождение (двоичный поиск Версии 1) */
    int L = 1, R = n, any = -1;
    while (L <= R) {
        int m = (L + R) / 2;
        (*C)++;
        if (arr[m-1] == X) { any = m - 1; break; }
        (*C)++;
        if (arr[m-1] < X) L = m + 1;
        else              R = m - 1;
    }
    if (any == -1) return 0;
    /* Расширить влево */
    int l = any, r = any;
    while (l > 0) {
        (*C)++;
        if (arr[l - 1] == X) l--; else break;
    }
    /* Расширить вправо */
    while (r < n - 1) {
        (*C)++;
        if (arr[r + 1] == X) r++; else break;
    }
    int cnt = 0;
    for (int i = l; i <= r; i++) found[cnt++] = i;
    return cnt;
}

