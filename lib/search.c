/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Поиск всех вхождений — двумя двоичными             ║
 * ║            (lower_bound + upper_bound)                       ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Альтернатива BSearchAll1: чисто логарифмическая.
 *  LowerBound находит первый индекс с arr[i] ≥ X (= самый левый);
 *  UpperBound — первый индекс с arr[i] > X (= за последним равным).
 *  Если LowerBound == UpperBound, элементов с ключом X нет. */
static int LowerBound(int n, int arr[], int X, int *C) {
    int L = 1, R = n + 1;
    while (L < R) {
        int m = (L + R) / 2;
        (*C)++;
        if (arr[m-1] < X) L = m + 1;
        else              R = m;
    }
    return L;                                         /* 1-based индекс */
}

static int UpperBound(int n, int arr[], int X, int *C) {
    int L = 1, R = n + 1;
    while (L < R) {
        int m = (L + R) / 2;
        (*C)++;
        if (arr[m-1] <= X) L = m + 1;
        else               R = m;
    }
    return L;                                         /* 1-based индекс */
}

int BSearchAll2(int n, int arr[n], int X, int found[], int *C) {
    *C = 0;
    int l = LowerBound(n, arr, X, C);
    int r = UpperBound(n, arr, X, C);
    if (l >= r) return 0;
    int cnt = 0;
    for (int i = l; i < r; i++) found[cnt++] = i - 1;   /* в found — 0-based */
    return cnt;
}

