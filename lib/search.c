/*  Алгоритм на псевдокоде:
 *      L := 1,  R := n
 *      DO (L < R)
 *          m := ⌊(L+R)/2⌋
 *          IF (a_m < X)  L := m + 1
 *          ELSE          R := m
 *          FI
 *      OD
 *      IF (a_L = X) Найден := да  ELSE Найден := нет  FI
 *
 *  Трудоёмкость: C = ⌈log₂n⌉ + 1 — одно сравнение на итерацию
 *  + одно финальное. Если в массиве несколько одинаковых ключей,
 *  находит САМЫЙ ЛЕВЫЙ из них.
 */
int BSearch2(int n, int arr[n], int X, int *C) {
    *C = 0;
    if (n == 0) return -1;
    int L = 1, R = n;
    while (L < R) {                                    /* DO (L < R) */
        int m = (L + R) / 2;
        (*C)++;
        if (arr[m-1] < X) L = m + 1;                    /* IF (a_m < X) L := m+1 */
        else              R = m;                        /* ELSE R := m */
    }
    (*C)++;
    if (arr[L-1] == X) return L - 1;                    /* IF (a_L = X) Найден := да */
    return -1;                                          /* ELSE Найден := нет */
}

