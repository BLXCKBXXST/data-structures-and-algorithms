/* ╔══════════════════════════════════════════════════════════════╗
 * ║   НАЧАЛО: Двоичный поиск, Версия 1 (методичка 7.1)           ║
 * ╚══════════════════════════════════════════════════════════════╝ */

/*  Алгоритм на псевдокоде:
 *      L := 1,  R := n,  Найден := нет
 *      DO (L ≤ R)
 *          m := ⌊(L+R)/2⌋
 *          IF (a_m = X)  Найден := да,  OD  FI
 *          IF (a_m < X)  L := m + 1
 *          ELSE          R := m - 1
 *          FI
 *      OD
 *
 *  Трудоёмкость: C ≤ 2·log₂n (на каждой итерации до двух сравнений).
 *  Если в массиве несколько элементов с одинаковым ключом, эта версия
 *  находит произвольный из них. Возвращает 0-based индекс найденного
 *  элемента или -1, если не найден.
 */
int BSearch1(int n, int arr[n], int X, int *C) {
    *C = 0;
    int L = 1, R = n;
    while (L <= R) {                                  /* DO (L ≤ R) */
        int m = (L + R) / 2;
        (*C)++;
        if (arr[m-1] == X) {                          /* IF (a_m = X) Найден := да OD FI */
            return m - 1;
        }
        (*C)++;
        if (arr[m-1] < X) L = m + 1;                   /* IF (a_m < X) L := m+1 */
        else              R = m - 1;                   /* ELSE R := m-1 */
    }
    return -1;                                         /* Найден := нет */
}

