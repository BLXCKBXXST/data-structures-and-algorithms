/*
 * Раздел 1.10 — QuickSort
 * Версия 1: опорный = средний элемент
 * Версия 2: опорный = медиана из трёх
 * Сравнение: M+C и глубина рекурсии для N=100..500
 */
#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <time.h>
#include "sorting.h"

/* Подсчёт числа серий для проверки правильности сортировки */
static int CountRuns(int n, int arr[n]) {
    if (n == 0) return 0;
    int runs = 1;
    for (int i = 1; i < n; i++)
        if (arr[i] < arr[i - 1]) runs++;
    return runs;
}

/* Контрольная сумма массива */
static long CheckSum(int n, int arr[n]) {
    long s = 0;
    for (int i = 0; i < n; i++) s += arr[i];
    return s;
}

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    /* ===================================================================
     * Таблица 1: QuickSort (версия 1) — теория вс факт (M+C)
     * =================================================================== */
    printf("\n=== Раздел 1.10: QuickSort ===\n");

    printf("\n┌──────┬────────────┬────────────┬────────────┬──────────────┬──────────────┬──────────────┐\n");
    printf(  "│      │              QuickSort (версия 1): Теория вс Факт (M+C)                         │\n");
    printf(  "│  N   ├────────────┬────────────┬────────────┬──────────────┬──────────────┬──────────────┤\n");
    printf(  "│      │ Mt+Ct(убыв)│ Mt+Ct(случ)│ Mt+Ct(возр)│  Mф+Cф(убыв) │  Mф+Cф(случ) │  Mф+Cф(возр) │\n");
    printf(  "├──────┼────────────┼────────────┼────────────┼──────────────┼──────────────┼──────────────┤\n");

    /* Сохраняем данные для таблиц */
    int M1dec[5], C1dec[5], M1rnd[5], C1rnd[5], M1inc[5], C1inc[5];
    int D1dec[5], D1rnd[5], D1inc[5];
    int M2dec[5], C2dec[5], M2rnd[5], C2rnd[5], M2inc[5], C2inc[5];
    int D2dec[5], D2rnd[5], D2inc[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        /* Теория: случайный M=n*log2(n), C=n*log2(n); asc/desc M=3(n-1), C=(n^2+5n+4)/2 */
        int MtCt_rnd = (int)(n * lg + n * lg);           /* M + C для случайного */
        int MtCt_worst = 3 * (n - 1) + (n * n + 5 * n + 4) / 2; /* для убыв/возр */

        int arr[n], orig[n];
        long cs_before, cs_after;

        /* Убывающий */
        FillDec(n, orig);
        cs_before = CheckSum(n, orig);
        CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1dec[s], &C1dec[s], &D1dec[s]);
        cs_after = CheckSum(n, arr);
        if (cs_before != cs_after || CountRuns(n, arr) != 1) {
            printf("ОШИБКА: QuickSort1 dec n=%d\n", n);
        }

        /* Случайный */
        FillRand(n, orig);
        cs_before = CheckSum(n, orig);
        CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1rnd[s], &C1rnd[s], &D1rnd[s]);
        cs_after = CheckSum(n, arr);
        if (cs_before != cs_after || CountRuns(n, arr) != 1) {
            printf("ОШИБКА: QuickSort1 rnd n=%d\n", n);
        }

        /* Возрастающий */
        FillInc(n, orig);
        cs_before = CheckSum(n, orig);
        CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1inc[s], &C1inc[s], &D1inc[s]);
        cs_after = CheckSum(n, arr);
        if (cs_before != cs_after || CountRuns(n, arr) != 1) {
            printf("ОШИБКА: QuickSort1 inc n=%d\n", n);
        }

        printf("│ %4d │ %10d │ %10d │ %10d │ %12d │ %12d │ %12d │\n",
            n, MtCt_worst, MtCt_rnd, MtCt_worst,
            M1dec[s] + C1dec[s], M1rnd[s] + C1rnd[s], M1inc[s] + C1inc[s]);
    }
    printf("└──────┴────────────┴────────────┴────────────┴──────────────┴──────────────┴──────────────┘\n");

    /* ===================================================================
     * Таблица 2: QuickSort (версия 2 — медиана из трёх) — теория вс факт
     * =================================================================== */
    printf("\n┌──────┬────────────┬────────────┬────────────┬──────────────┬──────────────┬──────────────┐\n");
    printf(  "│      │           QuickSort (версия 2, медиана-3): Теория вс Факт (M+C)                  │\n");
    printf(  "│  N   ├────────────┬────────────┬────────────┬──────────────┬──────────────┬──────────────┤\n");
    printf(  "│      │ Mt+Ct(убыв)│ Mt+Ct(случ)│ Mt+Ct(возр)│  Mф+Cф(убыв) │  Mф+Cф(случ) │  Mф+Cф(возр) │\n");
    printf(  "├──────┼────────────┼────────────┼────────────┼──────────────┼──────────────┼──────────────┤\n");

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        int MtCt_rnd = (int)(n * lg + n * lg);
        int MtCt_worst = 3 * (n - 1) + (n * n + 5 * n + 4) / 2;

        int arr[n], orig[n];

        FillDec(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2dec[s], &C2dec[s], &D2dec[s]);

        FillRand(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2rnd[s], &C2rnd[s], &D2rnd[s]);

        FillInc(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2inc[s], &C2inc[s], &D2inc[s]);

        printf("│ %4d │ %10d │ %10d │ %10d │ %12d │ %12d │ %12d │\n",
            n, MtCt_worst, MtCt_rnd, MtCt_worst,
            M2dec[s] + C2dec[s], M2rnd[s] + C2rnd[s], M2inc[s] + C2inc[s]);
    }
    printf("└──────┴────────────┴────────────┴────────────┴──────────────┴──────────────┴──────────────┘\n");

    /* ===================================================================
     * Таблица 3: Сравнение глубины рекурсии версий 1 и 2
     * =================================================================== */
    printf("\n┌──────┬──────────────────────────┬──────────────────────────┐\n");
    printf(  "│      │ Глубина рекурсии (вер. 1) │ Глубина рекурсии (вер. 2) │\n");
    printf(  "│  N   ├────────┬────────┬────────┼────────┬────────┬────────┤\n");
    printf(  "│      │  убыв  │  случ  │  возр  │  убыв  │  случ  │  возр  │\n");
    printf(  "├──────┼────────┼────────┼────────┼────────┼────────┼────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %6d │ %6d │ %6d │ %6d │ %6d │ %6d │\n",
            sizes[s],
            D1dec[s], D1rnd[s], D1inc[s],
            D2dec[s], D2rnd[s], D2inc[s]);
    }
    printf("└──────┴────────┴────────┴────────┴────────┴────────┴────────┘\n");

    /* ===================================================================
     * Таблица 4: Фактические M и C по отдельности (версия 1)
     * =================================================================== */
    printf("\n┌──────┬─────────┬─────────┬──────────┬─────────┬─────────┬──────────┬─────────┬─────────┬──────────┐\n");
    printf(  "│      │        QuickSort (вер.1): Фактические значения M и C по отдельности              │\n");
    printf(  "│  N   ├─────────┬─────────┬──────────┬─────────┬─────────┬──────────┬─────────┬─────────┬──────────┤\n");
    printf(  "│      │  M(убыв)│  C(убыв)│M+C(убыв) │  M(случ)│  C(случ)│M+C(случ) │  M(возр)│  C(возр)│M+C(возр) │\n");
    printf(  "├──────┼─────────┼─────────┼──────────┼─────────┼─────────┼──────────┼─────────┼─────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %7d │ %7d │ %8d │ %7d │ %7d │ %8d │ %7d │ %7d │ %8d │\n",
            sizes[s],
            M1dec[s], C1dec[s], M1dec[s]+C1dec[s],
            M1rnd[s], C1rnd[s], M1rnd[s]+C1rnd[s],
            M1inc[s], C1inc[s], M1inc[s]+C1inc[s]);
    }
    printf("└──────┴─────────┴─────────┴──────────┴─────────┴─────────┴──────────┴─────────┴─────────┴──────────┘\n");

    /* Вывод */
    printf("\n--- Выводы по QuickSort ---\n");
    printf("1. QuickSort значительно быстрее квадратичных методов: O(n*log(n)) на случайных данных.\n");
    printf("2. На упорядоченных массивах (убыв/возр) версия 1 (средний элемент) работает хорошо,\n");
    printf("   т.к. средний элемент близок к медиане.\n");
    printf("3. Версия 2 (медиана из трёх) даёт меньшую глубину рекурсии и более стабильную\n");
    printf("   производительность на различных входных данных.\n");
    printf("4. Глубина рекурсии: на случайных данных ~log2(n), на упорядоченных зависит от выбора опорного.\n");

    return 0;
}
