#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    /* Сохраняем данные */
    int Bmd[5], Bcd[5], Bmr[5], Bcr[5], Bmi[5], Bci[5];
    int Smd[5], Scd[5], Smr[5], Scr[5], Smi[5], Sci[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        RunSort(n, BubbleSort, &Bmd[s], &Bcd[s], &Bmr[s], &Bcr[s], &Bmi[s], &Bci[s]);
        RunSort(n, ShakerSort, &Smd[s], &Scd[s], &Smr[s], &Scr[s], &Smi[s], &Sci[s]);
    }

    /* ===================================================================
     * Таблица 1: Сравнение Bubble vs Shaker (ТОЧНО ПО ТЗ)
     * =================================================================== */
    printf("┌───────────────────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│              Трудоемкость пузырьковой и шейкерной сортировок                              │\n");
    printf("├──────┬───────────────────────────────────┬───────────────────────────────────┤\n");
    printf("│      │      Мф+Сф пузырьковой            │      Мф+Сф шейкерной              │\n");
    printf("│  n   ├───────────┬───────────┬────────────┼───────────┬───────────┬────────────┤\n");
    printf("│      │   Убыв.   │   Случ.   │   Возр.    │   Убыв.   │   Случ.   │   Возр.    │\n");
    printf("├──────┼───────────┼───────────┼────────────┼───────────┼───────────┼────────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %9d │ %9d │ %10d │ %9d │ %9d │ %10d │\n",
            sizes[s],
            Bmd[s] + Bcd[s], Bmr[s] + Bcr[s], Bmi[s] + Bci[s],
            Smd[s] + Scd[s], Smr[s] + Scr[s], Smi[s] + Sci[s]);
    }
    printf("└──────┴───────────┴───────────┴────────────┴───────────┴───────────┴────────────┘\n");

    /* ===================================================================
     * Таблица 2: ShakerSort — разбор M и C отдельно, факт + теория
     * =================================================================== */
    printf("\n┌──────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│                                  ShakerSort: Разбор пересылок и сравнений                                                    │\n");
    printf("├──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┤\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int Mtdec = 3 * n * (n - 1) / 2;
        int Ctdec = n * (n - 1) / 2;
        int Mtrnd = 3 * n * (n - 1) / 4;
        int Ctrnd = n * (n - 1) / 2;
        int Mtinc = 0;
        int Ctinc = n - 1;
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mtdec, Ctdec, Smd[s], Scd[s], Mtrnd, Ctrnd, Smr[s], Scr[s], Mtinc, Ctinc, Smi[s], Sci[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    /* Выводы */
    printf("\nВывод: ShakerSort значительно эффективнее BubbleSort на возрастающих массивах (Cmin=n-1 vs C=(n²-n)/2).\n");
    printf("Вывод: На убывающих массивах — одинаково. На случайных — ShakerSort немного лучше.\n");

    return 0;
}
