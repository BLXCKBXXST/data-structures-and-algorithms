#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    int Bmd[5], Bcd[5], Bmr[5], Bcr[5], Bmi[5], Bci[5];
    int Smd[5], Scd[5], Smr[5], Scr[5], Smi[5], Sci[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        RunSort(n, BubbleSort, &Bmd[s], &Bcd[s], &Bmr[s], &Bcr[s], &Bmi[s], &Bci[s]);
        RunSort(n, ShakerSort, &Smd[s], &Scd[s], &Smr[s], &Scr[s], &Smi[s], &Sci[s]);
    }

    /* ===================================================================
     * Основная таблица ТЗ: Трудоемкость пузырьковой и шейкерной сортировок
     * =================================================================== */
    printf("                            Трудоемкость пузырьковой и шейкерной сортировок\n");
    printf("┌──────┬─────────────────────────────────────┬─────────────────────────────────────┐\n");
    printf("│      │      Мф+Сф пузырьковой              │      Мф+Сф шейкерной                │\n");
    printf("│  n   ├───────────┬───────────┬─────────────┼───────────┬───────────┬─────────────┤\n");
    printf("│      │   Убыв.   │   Случ.   │    Возр.    │   Убыв.   │   Случ.   │    Возр.    │\n");
    printf("├──────┼───────────┼───────────┼─────────────┼───────────┼───────────┼─────────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %9d │ %9d │ %11d │ %9d │ %9d │ %11d │\n",
            sizes[s],
            Bmd[s] + Bcd[s], Bmr[s] + Bcr[s], Bmi[s] + Bci[s],
            Smd[s] + Scd[s], Smr[s] + Scr[s], Smi[s] + Sci[s]);
    }
    printf("└──────┴───────────┴───────────┴─────────────┴───────────┴───────────┴─────────────┘\n");

    /* ===================================================================
     * === Доп. === ShakerSort: теория vs факт по M и C
     * Теория:
     *   Убыв.: Mmax = 3(n²-n)/2,  Cmax = (n²-n)/2
     *   Случ.: Mср  = 3(n²-n)/4,  Cср ≤ (n²-n)/2
     *   Возр.: Mmin = 0,          Cmin = n-1
     * =================================================================== */
    printf("\n=== Доп. ===\n");
    printf("                                              ShakerSort: теория vs факт по M и C\n");
    printf("┌──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┐\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int Mtdec = 3 * n * (n - 1) / 2;
        int Ctdec = n * (n - 1) / 2;
        int Mtrnd = 3 * n * (n - 1) / 4;
        int Ctrnd = n * (n - 1) / 2;
        int Mtinc = 0;
        int Ctinc = n - 1;
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mtdec, Ctdec, Smd[s], Scd[s], Mtrnd, Ctrnd, Smr[s], Scr[s], Mtinc, Ctinc, Smi[s], Sci[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    return 0;
}
