#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {10, 100};

    int Mo_dec[2], Co_dec[2], Mo_rnd[2], Co_rnd[2], Mo_inc[2], Co_inc[2];
    int Mi_dec[2], Ci_dec[2], Mi_rnd[2], Ci_rnd[2], Mi_inc[2], Ci_inc[2];

    for (int s = 0; s < 2; s++) {
        int n = sizes[s];
        int A[n], B[n], C[n], arr[n];
        int M, Cv;

        FillInc(n, A); FillDec(n, B); FillRand(n, C);

        CopyArray(n, B, arr); SelectSort(n, arr, &M, &Cv);
        Mo_dec[s] = M; Co_dec[s] = Cv;
        CopyArray(n, C, arr); SelectSort(n, arr, &M, &Cv);
        Mo_rnd[s] = M; Co_rnd[s] = Cv;
        CopyArray(n, A, arr); SelectSort(n, arr, &M, &Cv);
        Mo_inc[s] = M; Co_inc[s] = Cv;

        CopyArray(n, B, arr); SelectSortImproved(n, arr, &M, &Cv);
        Mi_dec[s] = M; Ci_dec[s] = Cv;
        CopyArray(n, C, arr); SelectSortImproved(n, arr, &M, &Cv);
        Mi_rnd[s] = M; Ci_rnd[s] = Cv;
        CopyArray(n, A, arr); SelectSortImproved(n, arr, &M, &Cv);
        Mi_inc[s] = M; Ci_inc[s] = Cv;
    }

    /* ===================================================================
     * Основная таблица ТЗ: Трудоемкость метода прямого выбора
     * (M+C теоретич. одинаков для всех случаев: 3(n-1) + (n²-n)/2)
     * =================================================================== */
    printf("                                  Трудоемкость метода прямого выбора\n");
    printf("┌──────┬─────────────────────────────────┬─────────────────────────────────┬─────────────────────────────────┐\n");
    printf("│      │       M+C теоретич.             │     Исходный Мфакт+Сфакт        │   Улучшенный Мфакт+Сфакт        │\n");
    printf("│  N   ├──────────┬──────────┬───────────┼──────────┬──────────┬───────────┼──────────┬──────────┬───────────┤\n");
    printf("│      │  Убыв.   │  Случ.   │   Возр.   │  Убыв.   │  Случ.   │   Возр.   │  Убыв.   │  Случ.   │   Возр.   │\n");
    printf("├──────┼──────────┼──────────┼───────────┼──────────┼──────────┼───────────┼──────────┼──────────┼───────────┤\n");
    for (int s = 0; s < 2; s++) {
        int n = sizes[s];
        int MC = 3 * (n - 1) + n * (n - 1) / 2;
        printf("│ %4d │ %8d │ %8d │ %9d │ %8d │ %8d │ %9d │ %8d │ %8d │ %9d │\n",
            n, MC, MC, MC,
            Mo_dec[s] + Co_dec[s], Mo_rnd[s] + Co_rnd[s], Mo_inc[s] + Co_inc[s],
            Mi_dec[s] + Ci_dec[s], Mi_rnd[s] + Ci_rnd[s], Mi_inc[s] + Ci_inc[s]);
    }
    printf("└──────┴──────────┴──────────┴───────────┴──────────┴──────────┴───────────┴──────────┴──────────┴───────────┘\n");

    /* ===================================================================
     * === Доп. === Детальный разбор M и C: теория vs факт
     * Теория (для всех случаев): M = 3(n-1), C = (n²-n)/2
     * =================================================================== */
    printf("\n=== Доп. ===\n");
    printf("                       SelectSort (исходный): теория vs факт по M и C\n");
    printf("┌──────┬─────────┬─────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┐\n");
    printf("│  N   │ Mt теор │ Ct теор │ Мф(убыв) │ Сф(убыв) │ Мф(случ) │ Сф(случ) │ Мф(возр) │ Сф(возр) │\n");
    printf("├──────┼─────────┼─────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 2; s++) {
        int n = sizes[s];
        int Mt = 3 * (n - 1);
        int Ct = n * (n - 1) / 2;
        printf("│ %4d │ %7d │ %7d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mt, Ct,
            Mo_dec[s], Co_dec[s], Mo_rnd[s], Co_rnd[s], Mo_inc[s], Co_inc[s]);
    }
    printf("└──────┴─────────┴─────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    printf("\n                      SelectSort (улучшенный): теория vs факт по M и C\n");
    printf("┌──────┬─────────┬─────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┐\n");
    printf("│  N   │ Mt теор │ Ct теор │ Мф(убыв) │ Сф(убыв) │ Мф(случ) │ Сф(случ) │ Мф(возр) │ Сф(возр) │\n");
    printf("├──────┼─────────┼─────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 2; s++) {
        int n = sizes[s];
        int Mt = 3 * (n - 1);
        int Ct = n * (n - 1) / 2;
        printf("│ %4d │ %7d │ %7d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mt, Ct,
            Mi_dec[s], Ci_dec[s], Mi_rnd[s], Ci_rnd[s], Mi_inc[s], Ci_inc[s]);
    }
    printf("└──────┴─────────┴─────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    return 0;
}
