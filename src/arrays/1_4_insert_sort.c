#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    int Md[5], Cd[5], Mr[5], Cr[5], Mi_a[5], Ci_a[5];
    int sel[5], bub[5], shk[5], ins[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        RunSort(n, InsertSort, &Md[s], &Cd[s], &Mr[s], &Cr[s], &Mi_a[s], &Ci_a[s]);
    }

    /* ===================================================================
     * РһСҒРҪРҫРІРҪР°СҸ СӮР°РұР»РёСҶР° РўР—: РўСҖСғРҙРҫРөРјРәРҫСҒСӮСҢ РјРөСӮРҫРҙР° РҝСҖСҸРјРҫРіРҫ РІРәР»СҺСҮРөРҪРёСҸ InsertSort
     * РўРөРҫСҖРёСҸ M+C РҝРҫ СӮРёРҝР°Рј:
     *   РЈРұСӢРІ.: (nВІ-n)/2 + 2(n-1) + (nВІ-n)/2 = (nВІ-n) + 2(n-1)
     *   РЎР»СғСҮ.: (nВІ-n)/4 + (n-1)  + (nВІ-n)/4 = (nВІ-n)/2 + (n-1)
     *   Р’РҫР·СҖ.: 2(n-1)            + (n-1)    = 3(n-1)
     * =================================================================== */
    printf("                            РўСҖСғРҙРҫРөРјРәРҫСҒСӮСҢ РјРөСӮРҫРҙР° РҝСҖСҸРјРҫРіРҫ РІРәР»СҺСҮРөРҪРёСҸ InsertSort\n");
    printf("в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ\n");
    printf("в”Ӯ      в”Ӯ           M+C СӮРөРҫСҖРөСӮРёСҮ.             в”Ӯ           РңС„Р°РәСӮ+РЎС„Р°РәСӮ               в”Ӯ\n");
    printf("в”Ӯ  N   в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө\n");
    printf("в”Ӯ      в”Ӯ   РЈРұСӢРІ.   в”Ӯ   РЎР»СғСҮ.   в”Ӯ    Р’РҫР·СҖ.    в”Ӯ   РЈРұСӢРІ.   в”Ӯ   РЎР»СғСҮ.   в”Ӯ    Р’РҫР·СҖ.    в”Ӯ\n");
    printf("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int MCdec = n * (n - 1) + 2 * (n - 1);
        int MCrnd = n * (n - 1) / 2 + (n - 1);
        int MCinc = 3 * (n - 1);
        printf("в”Ӯ %4d в”Ӯ %9d в”Ӯ %9d в”Ӯ %11d в”Ӯ %9d в”Ӯ %9d в”Ӯ %11d в”Ӯ\n",
            n, MCdec, MCrnd, MCinc,
            Md[s] + Cd[s], Mr[s] + Cr[s], Mi_a[s] + Ci_a[s]);
    }
    printf("в””в”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ\n");

    /* ===================================================================
     * РһСҒРҪРҫРІРҪР°СҸ СӮР°РұР»РёСҶР° РўР—: РўСҖСғРҙРҫРөРјРәРҫСҒСӮСҢ РәРІР°РҙСҖР°СӮРёСҮРҪСӢС… РјРөСӮРҫРҙРҫРІ СҒРҫСҖСӮРёСҖРҫРІРәРё
     * (РҪР° РјР°СҒСҒРёРІР°С… СҒР»СғСҮР°Р№РҪСӢС… СҮРёСҒРөР»)
     * =================================================================== */
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int src[n], arr[n];
        int M, C;
        FillRand(n, src);
        CopyArray(n, src, arr); SelectSort(n, arr, &M, &C); sel[s] = M + C;
        CopyArray(n, src, arr); BubbleSort(n, arr, &M, &C); bub[s] = M + C;
        CopyArray(n, src, arr); ShakerSort(n, arr, &M, &C); shk[s] = M + C;
        CopyArray(n, src, arr); InsertSort(n, arr, &M, &C); ins[s] = M + C;
    }

    printf("\n                РўСҖСғРҙРҫРөРјРәРҫСҒСӮСҢ РәРІР°РҙСҖР°СӮРёСҮРҪСӢС… РјРөСӮРҫРҙРҫРІ СҒРҫСҖСӮРёСҖРҫРІРәРё (СҒР»СғСҮР°Р№РҪСӢРө СҮРёСҒР»Р°)\n");
    printf("в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ\n");
    printf("в”Ӯ      в”Ӯ                          РңС„+РЎС„                              в”Ӯ\n");
    printf("в”Ӯ  n   в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө\n");
    printf("в”Ӯ      в”Ӯ    Select    в”Ӯ    Bubble    в”Ӯ    Shaker    в”Ӯ     Insert     в”Ӯ\n");
    printf("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө\n");
    for (int s = 0; s < 5; s++) {
        printf("в”Ӯ %4d в”Ӯ %12d в”Ӯ %12d в”Ӯ %12d в”Ӯ %14d в”Ӯ\n",
            sizes[s], sel[s], bub[s], shk[s], ins[s]);
    }
    printf("в””в”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ\n");

    /* ===================================================================
     * === Р”РҫРҝ. === InsertSort: СӮРөРҫСҖРёСҸ vs С„Р°РәСӮ РҝРҫ M Рё C
     * РўРөРҫСҖРёСҸ:
     *   РЈРұСӢРІ.: Mmax = (nВІ-n)/2 + 2(n-1),  Cmax = (nВІ-n)/2
     *   РЎР»СғСҮ.: MСҒСҖ  = (nВІ-n)/4 + (n-1),   CСҒСҖ  = (nВІ-n)/4
     *   Р’РҫР·СҖ.: Mmin = 2(n-1),             Cmin = n-1
     * =================================================================== */
    printf("\n=== Р”РҫРҝ. ===\n");
    printf("                                              InsertSort: СӮРөРҫСҖРёСҸ vs С„Р°РәСӮ РҝРҫ M Рё C\n");
    printf("в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ\n");
    printf("в”Ӯ  N   в”ӮMt(СғРұСӢРІ)  в”ӮCt(СғРұСӢРІ)  в”ӮРңС„(СғРұСӢРІ)  в”ӮРЎС„(СғРұСӢРІ)  в”ӮMt(СҒР»СғСҮ)  в”ӮCt(СҒР»СғСҮ)  в”ӮРңС„(СҒР»СғСҮ)  в”ӮРЎС„(СҒР»СғСҮ)  в”ӮMt(РІРҫР·СҖ)  в”ӮCt(РІРҫР·СҖ)  в”ӮРңС„(РІРҫР·СҖ)  в”ӮРЎС„(РІРҫР·СҖ)  в”Ӯ\n");
    printf("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int Mtdec = n * (n - 1) / 2 + 2 * (n - 1);
        int Ctdec = n * (n - 1) / 2;
        int Mtrnd = n * (n - 1) / 4 + (n - 1);
        int Ctrnd = n * (n - 1) / 4;
        int Mtinc = 2 * (n - 1);
        int Ctinc = n - 1;
        printf("в”Ӯ %4d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ %8d в”Ӯ\n",
            n, Mtdec, Ctdec, Md[s], Cd[s], Mtrnd, Ctrnd, Mr[s], Cr[s], Mtinc, Ctinc, Mi_a[s], Ci_a[s]);
    }
    printf("в””в”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ\n");

    return 0;
}
