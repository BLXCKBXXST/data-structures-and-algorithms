/*
 * Раздел 1.10 — QuickSort (метод Хоара)
 * Версия 1: рекурсивный метод Хоара (методичка 4.2), опорный = a_L.
 * Версия 2: метод Хоара с уменьшением глубины рекурсии (методичка 4.3) —
 *           рекурсия только для меньшей части, бо́льшая итерируется в while.
 *           Глубина рекурсии ≤ log₂(n) даже в худшем случае.
 *
 * Теоретические формулы (по таблице трудоёмкости):
 *   Лучший случай:   M = n·log2(n),  C = n·log2(n)
 *   Худший случай:   M = 3(n-1),     C = (n² + 5n + 4) / 2
 *
 * Для обеих версий опорный = a_L:
 *   Убыв./Возр. → опорный всегда крайний → ХУДШИЙ случай (по M+C)
 *   Случ.       → опорный близок к медиане → ЛУЧШИЙ случай (по M+C)
 *   У версии 2 такие же M, C, но глубина рекурсии существенно меньше.
 */
#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    int M1d[5], C1d[5], M1r[5], C1r[5], M1i[5], C1i[5];
    int D1d[5], D1r[5], D1i[5];
    int M2d[5], C2d[5], M2r[5], C2r[5], M2i[5], C2i[5];
    int D2d[5], D2r[5], D2i[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int arr[n], orig[n];

        FillDec(n, orig); CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1d[s], &C1d[s], &D1d[s]);
        FillRand(n, orig); CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1r[s], &C1r[s], &D1r[s]);
        FillInc(n, orig); CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1i[s], &C1i[s], &D1i[s]);

        FillDec(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2d[s], &C2d[s], &D2d[s]);
        FillRand(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2r[s], &C2r[s], &D2r[s]);
        FillInc(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2i[s], &C2i[s], &D2i[s]);
    }

    /* ===================================================================
     * Основная таблица ТЗ: Трудоемкость метода Хоара (QuickSort1)
     * Теория M+C по типам:
     *   Убыв.: 3(n-1) + (n²+5n+4)/2  (худший)
     *   Случ.: 2·n·log2(n)           (лучший)
     *   Возр.: 3(n-1) + (n²+5n+4)/2  (худший)
     * =================================================================== */
    printf("                              Трудоемкость метода Хоара (QuickSort1)\n");
    printf("┌──────┬─────────────────────────────────────┬─────────────────────────────────────┐\n");
    printf("│      │           M+C теоретич.             │       QuickSort1 (Мф+Сф)            │\n");
    printf("│  N   ├───────────┬───────────┬─────────────┼───────────┬───────────┬─────────────┤\n");
    printf("│      │   Убыв.   │   Случ.   │    Возр.    │   Убыв.   │   Случ.   │    Возр.    │\n");
    printf("├──────┼───────────┼───────────┼─────────────┼───────────┼───────────┼─────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        int MCworst = 3 * (n - 1) + (n * n + 5 * n + 4) / 2;
        int MCbest  = (int)(2.0 * n * lg);
        printf("│ %4d │ %9d │ %9d │ %11d │ %9d │ %9d │ %11d │\n",
            n, MCworst, MCbest, MCworst,
            M1d[s] + C1d[s], M1r[s] + C1r[s], M1i[s] + C1i[s]);
    }
    printf("└──────┴───────────┴───────────┴─────────────┴───────────┴───────────┴─────────────┘\n");

    /* ===================================================================
     * Основная таблица ТЗ (п.4*): Глубина рекурсии — QuickSort1 vs QuickSort2
     * =================================================================== */
    printf("\n                          Глубина рекурсии сортировки методом Хоара\n");
    printf("┌──────┬─────────────────────────────────┬─────────────────────────────────┐\n");
    printf("│      │           QuickSort1            │           QuickSort2            │\n");
    printf("│  n   ├──────────┬──────────┬───────────┼──────────┬──────────┬───────────┤\n");
    printf("│      │  Убыв.   │  Случ.   │   Возр.   │  Убыв.   │  Случ.   │   Возр.   │\n");
    printf("├──────┼──────────┼──────────┼───────────┼──────────┼──────────┼───────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %8d │ %8d │ %9d │ %8d │ %8d │ %9d │\n",
            sizes[s],
            D1d[s], D1r[s], D1i[s],
            D2d[s], D2r[s], D2i[s]);
    }
    printf("└──────┴──────────┴──────────┴───────────┴──────────┴──────────┴───────────┘\n");

    /* ===================================================================
     * === Доп. === QuickSort1: разбор M и C — теория vs факт
     * Маппинг по типам:
     *   Убыв./Возр. → худший: M=3(n-1),       C=(n²+5n+4)/2
     *   Случ.       → лучший: M=n·log2(n),    C=n·log2(n)
     * =================================================================== */
    printf("\n=== Доп. ===\n");
    printf("                                              QuickSort1: теория vs факт по M и C\n");
    printf("┌──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┐\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        int Mt_w = 3 * (n - 1);
        int Ct_w = (n * n + 5 * n + 4) / 2;
        int Mt_b = (int)(n * lg);
        int Ct_b = (int)(n * lg);
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n,
            Mt_w, Ct_w, M1d[s], C1d[s],
            Mt_b, Ct_b, M1r[s], C1r[s],
            Mt_w, Ct_w, M1i[s], C1i[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    return 0;
}
