#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    int Md[5], Cd[5], Mr[5], Cr[5], Mi[5], Ci[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        RunSort(n, BubbleSort, &Md[s], &Cd[s], &Mr[s], &Cr[s], &Mi[s], &Ci[s]);
    }

    /* ===================================================================
     * Основная таблица ТЗ: Трудоемкость пузырьковой сортировки
     * Теория M+C по типам:
     *   Убыв.: 3(n²-n)/2 + (n²-n)/2 = 2(n²-n)
     *   Случ.: 3(n²-n)/4 + (n²-n)/2 = 5(n²-n)/4
     *   Возр.: 0          + (n²-n)/2 = (n²-n)/2
     * =================================================================== */
    printf("                                  Трудоемкость пузырьковой сортировки\n");
    printf("┌──────┬─────────────────────────────────────┬─────────────────────────────────────┐\n");
    printf("│      │           M+C теоретич.             │           Мфакт+Сфакт               │\n");
    printf("│  N   ├───────────┬───────────┬─────────────┼───────────┬───────────┬─────────────┤\n");
    printf("│      │   Убыв.   │   Случ.   │    Возр.    │   Убыв.   │   Случ.   │    Возр.    │\n");
    printf("├──────┼───────────┼───────────┼─────────────┼───────────┼───────────┼─────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int MCdec = 2 * n * (n - 1);
        int MCrnd = 5 * n * (n - 1) / 4;
        int MCinc = n * (n - 1) / 2;
        printf("│ %4d │ %9d │ %9d │ %11d │ %9d │ %9d │ %11d │\n",
            n, MCdec, MCrnd, MCinc,
            Md[s] + Cd[s], Mr[s] + Cr[s], Mi[s] + Ci[s]);
    }
    printf("└──────┴───────────┴───────────┴─────────────┴───────────┴───────────┴─────────────┘\n");

    /* ===================================================================
     * === Доп. === Детальный разбор M и C: теория vs факт
     * Теория:
     *   Убыв.: M = 3(n²-n)/2,  C = (n²-n)/2
     *   Случ.: M = 3(n²-n)/4,  C = (n²-n)/2
     *   Возр.: M = 0,          C = (n²-n)/2
     * =================================================================== */
    printf("\n=== Доп. ===\n");
    printf("                                              BubbleSort: теория vs факт по M и C\n");
    printf("┌──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┐\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int Ct = n * (n - 1) / 2;
        int Mtdec = 3 * n * (n - 1) / 2;
        int Mtrnd = 3 * n * (n - 1) / 4;
        int Mtinc = 0;
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mtdec, Ct, Md[s], Cd[s], Mtrnd, Ct, Mr[s], Cr[s], Mtinc, Ct, Mi[s], Ci[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    return 0;
}
