#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    int BHmd[5], BHcd[5], BHmr[5], BHcr[5], BHmi[5], BHci[5];
    int Hmd[5],  Hcd[5],  Hmr[5],  Hcr[5],  Hmi[5],  Hci[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int arr[n], orig[n];

        FillDec(n, orig); CopyArray(n, orig, arr);
        BuildHeap(n, arr, &BHmd[s], &BHcd[s]);
        FillRand(n, orig); CopyArray(n, orig, arr);
        BuildHeap(n, arr, &BHmr[s], &BHcr[s]);
        FillInc(n, orig); CopyArray(n, orig, arr);
        BuildHeap(n, arr, &BHmi[s], &BHci[s]);

        FillDec(n, orig); CopyArray(n, orig, arr);
        HeapSort(n, arr, &Hmd[s], &Hcd[s]);
        FillRand(n, orig); CopyArray(n, orig, arr);
        HeapSort(n, arr, &Hmr[s], &Hcr[s]);
        FillInc(n, orig); CopyArray(n, orig, arr);
        HeapSort(n, arr, &Hmi[s], &Hci[s]);
    }

    /* ===================================================================
     * Основная таблица ТЗ: Трудоемкость построения пирамиды
     * Теория (точная оценка): M+C = 4n - 2·log2(n) - 4 (одна формула для всех типов)
     * =================================================================== */
    printf("                                  Трудоемкость построения пирамиды\n");
    printf("┌──────┬─────────────────────────────────────┬─────────────────────────────────────┐\n");
    printf("│      │           M+C теоретич.             │           Мфакт+Сфакт               │\n");
    printf("│  N   ├───────────┬───────────┬─────────────┼───────────┬───────────┬─────────────┤\n");
    printf("│      │   Убыв.   │   Случ.   │    Возр.    │   Убыв.   │   Случ.   │    Возр.    │\n");
    printf("├──────┼───────────┼───────────┼─────────────┼───────────┼───────────┼─────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int MCth = 4 * n - 2 * (int)log2((double)n) - 4;
        printf("│ %4d │ %9d │ %9d │ %11d │ %9d │ %9d │ %11d │\n",
            n, MCth, MCth, MCth,
            BHmd[s] + BHcd[s], BHmr[s] + BHcr[s], BHmi[s] + BHci[s]);
    }
    printf("└──────┴───────────┴───────────┴─────────────┴───────────┴───────────┴─────────────┘\n");

    /* ===================================================================
     * Основная таблица ТЗ: Трудоемкость пирамидальной сортировки
     * Теория: M+C = (n·log2(n) + 6.5n - 4) + (2n·log2(n) + n + 2)
     *             = 3n·log2(n) + 7.5n - 2  (одна формула для всех случаев)
     * =================================================================== */
    printf("\n                              Трудоемкость пирамидальной сортировки\n");
    printf("┌──────┬─────────────────────────────────────┬─────────────────────────────────────┐\n");
    printf("│      │           M+C теоретич.             │       HeapSort (Мф+Сф)              │\n");
    printf("│  N   ├───────────┬───────────┬─────────────┼───────────┬───────────┬─────────────┤\n");
    printf("│      │   Убыв.   │   Случ.   │    Возр.    │   Убыв.   │   Случ.   │    Возр.    │\n");
    printf("├──────┼───────────┼───────────┼─────────────┼───────────┼───────────┼─────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        int MCth = (int)(3.0 * n * lg + 7.5 * n - 2.0);
        printf("│ %4d │ %9d │ %9d │ %11d │ %9d │ %9d │ %11d │\n",
            n, MCth, MCth, MCth,
            Hmd[s] + Hcd[s], Hmr[s] + Hcr[s], Hmi[s] + Hci[s]);
    }
    printf("└──────┴───────────┴───────────┴─────────────┴───────────┴───────────┴─────────────┘\n");

    /* ===================================================================
     * === Доп. === HeapSort: разбор M и C — теория vs факт
     * Теория: M = n·log2(n) + 6.5n - 4,  C = 2n·log2(n) + n + 2
     * =================================================================== */
    printf("\n=== Доп. ===\n");
    printf("                                              HeapSort: теория vs факт по M и C\n");
    printf("┌──────┬─────────┬─────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┐\n");
    printf("│  N   │ Mt теор │ Ct теор │ Мф(убыв) │ Сф(убыв) │ Мф(случ) │ Сф(случ) │ Мф(возр) │ Сф(возр) │\n");
    printf("├──────┼─────────┼─────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        int Mt = (int)(n * lg + 6.5 * n - 4);
        int Ct = (int)(2.0 * n * lg + n + 2);
        printf("│ %4d │ %7d │ %7d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mt, Ct,
            Hmd[s], Hcd[s], Hmr[s], Hcr[s], Hmi[s], Hci[s]);
    }
    printf("└──────┴─────────┴─────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    return 0;
}
