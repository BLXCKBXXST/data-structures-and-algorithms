#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    /* Сохраняем данные для обеих таблиц */
    int Md[5], Cd[5], Mr[5], Cr[5], Mi[5], Ci[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        RunSort(n, BubbleSort, &Md[s], &Cd[s], &Mr[s], &Cr[s], &Mi[s], &Ci[s]);
    }

    /* ===================================================================
     * Таблица 1: Трудоемкость пузырьковой сортировки (ТОЧНО ПО ТЗ)
     * ОДНА колонка M+C теоретич. (средний случай)
     * =================================================================== */
    printf("┌────────────────────────────────────────────────────────────────────┐\n");
    printf("│              Трудоемкость пузырьковой сортировки                   │\n");
    printf("├──────┬───────────────┬─────────────────────────────────────────────┤\n");
    printf("│      │               │            Мфакт+Сфакт                     │\n");
    printf("│  N   │ M+C теоретич. ├─────────────┬─────────────┬────────────────┤\n");
    printf("│      │               │    Убыв.    │    Случ.    │     Возр.      │\n");
    printf("├──────┼───────────────┼─────────────┼─────────────┼────────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int MCtheor = 3 * n * (n - 1) / 4 + n * (n - 1) / 2;
        printf("│ %4d │ %13d │ %11d │ %11d │ %14d │\n",
            n, MCtheor,
            Md[s] + Cd[s], Mr[s] + Cr[s], Mi[s] + Ci[s]);
    }
    printf("└──────┴───────────────┴─────────────┴─────────────┴────────────────┘\n");

    /* ===================================================================
     * Таблица 2: BubbleSort — разбор M и C отдельно, факт + теория
     * =================================================================== */
    printf("\n┌──────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│                                  BubbleSort: Разбор пересылок и сравнений                                                    │\n");
    printf("├──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┤\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int Ct = n * (n - 1) / 2;
        int Mtdec = 3 * n * (n - 1) / 2;
        int Mtrnd = 3 * n * (n - 1) / 4;
        int Mtinc = 0;
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mtdec, Ct, Md[s], Cd[s], Mtrnd, Ct, Mr[s], Cr[s], Mtinc, Ct, Mi[s], Ci[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    /* Выводы */
    printf("\nВывод: BubbleSort зависит от упорядоченности по M (0 для возрастающего, max для убывающего), C всегда = (n²-n)/2.\n");

    return 0;
}
