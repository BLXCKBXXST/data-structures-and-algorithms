#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    /* Сохраняем данные */
    int Md[5], Cd[5], Mr[5], Cr[5], Mi_a[5], Ci_a[5];
    /* Для сводной таблицы */
    int sel[5], bub[5], shk[5], ins[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        RunSort(n, InsertSort, &Md[s], &Cd[s], &Mr[s], &Cr[s], &Mi_a[s], &Ci_a[s]);
    }

    /* ===================================================================
     * Таблица 1: Трудоемкость InsertSort (ТОЧНО ПО ТЗ)
     * ОДНА колонка M+C теоретич. (средний случай)
     * =================================================================== */
    printf("┌────────────────────────────────────────────────────────────────────────┐\n");
    printf("│           Трудоемкость метода прямого включения InsertSort             │\n");
    printf("├──────┬───────────────┬─────────────────────────────────────────────────┤\n");
    printf("│      │               │            Мфакт+Сфакт                         │\n");
    printf("│  N   │ M+C теоретич. ├─────────────┬─────────────┬────────────────┤\n");
    printf("│      │               │    Убыв.    │    Случ.    │     Возр.      │\n");
    printf("├──────┼───────────────┼─────────────┼─────────────┼────────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int MCtheor = n * (n - 1) / 2 + n - 1;
        printf("│ %4d │ %13d │ %11d │ %11d │ %14d │\n",
            n, MCtheor,
            Md[s] + Cd[s], Mr[s] + Cr[s], Mi_a[s] + Ci_a[s]);
    }
    printf("└──────┴───────────────┴─────────────┴─────────────┴────────────────┘\n");

    /* ===================================================================
     * Таблица 2: Сводная квадратичных методов (случайный массив, ТОЧНО ПО ТЗ)
     * =================================================================== */
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int src[n], arr[n];
        int M, C;
        FillRand(n, src);
        CopyArray(n, src, arr); SelectSort(n, arr, &M, &C); sel[s] = M + C;
        CopyArray(n, src, arr); BubbleSort(n, arr, &M, &C); bub[s] = M + C;
        CopyArray(n, src, arr); ShakerSort(n, arr, &M, &C); shk[s] = M + C;
        CopyArray(n, src, arr); InsertSort(n, arr, &M, &C); ins[s] = M + C;
    }

    printf("\n┌──────────────────────────────────────────────────────────────┐\n");
    printf("│         Трудоемкость квадратичных методов сортировки        │\n");
    printf("├──────┬───────────────────────────────────────────────────────┤\n");
    printf("│      │                     Мф+Сф                            │\n");
    printf("│  n   ├─────────────┬─────────────┬─────────────┬────────────┤\n");
    printf("│      │   Select    │   Bubble    │   Shaker    │   Insert   │\n");
    printf("├──────┼─────────────┼─────────────┼─────────────┼────────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %11d │ %11d │ %11d │ %10d │\n",
            sizes[s], sel[s], bub[s], shk[s], ins[s]);
    }
    printf("└──────┴─────────────┴─────────────┴─────────────┴────────────┘\n");

    /* ===================================================================
     * Таблица 3: InsertSort — разбор M и C отдельно, факт + теория
     * =================================================================== */
    printf("\n┌──────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│                                  InsertSort: Разбор пересылок и сравнений                                                    │\n");
    printf("├──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┤\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int Mtdec = n * (n - 1) / 2 + 2 * (n - 1);
        int Ctdec = n * (n - 1) / 2;
        int Mtrnd = n * (n - 1) / 4 + n - 1;
        int Ctrnd = n * (n - 1) / 4;
        int Mtinc = 2 * (n - 1);
        int Ctinc = n - 1;
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n, Mtdec, Ctdec, Md[s], Cd[s], Mtrnd, Ctrnd, Mr[s], Cr[s], Mtinc, Ctinc, Mi_a[s], Ci_a[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    /* Выводы */
    printf("\nВывод: InsertSort на возрастающем массиве работает за O(n) — самый быстрый из квадратичных.\n");
    printf("Вывод: На случайных данных InsertSort обычно быстрее BubbleSort и сравним с SelectSort.\n");

    return 0;
}
