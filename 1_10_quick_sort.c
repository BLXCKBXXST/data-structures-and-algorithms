/*
 * Раздел 1.10 — QuickSort
 * Версия 1: опорный = средний элемент
 * Версия 2: опорный = медиана из трёх
 *
 * Теоретические формулы (по ТЗ):
 *   Лучший случай:  M = n*log2(n),          C = n*log2(n)
 *   Худший/Средний:  M = 3*(n-1),           C = (n^2 + 5n + 4) / 2
 *
 * Для QS1 (средний элемент как опорный):
 *   — убывающий/возрастающий: опорный ≈ медиана → лучший случай
 *   — случайный: средний/худший случай
 */
#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <time.h>
#include "sorting.h"

int main(void) {
    srand(time(NULL));
    int sizes[] = {100, 200, 300, 400, 500};

    int M1d[5], C1d[5], M1r[5], C1r[5], M1i[5], C1i[5];
    int D1d[5], D1r[5], D1i[5];
    int M2d[5], C2d[5], M2r[5], C2r[5], M2i[5], C2i[5];
    int D2d[5], D2r[5], D2i[5];

    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        int arr[n], orig[n];

        FillDec(n, orig); CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1d[s], &C1d[s], &D1d[s]);

        FillRand(n, orig); CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1r[s], &C1r[s], &D1r[s]);

        FillInc(n, orig); CopyArray(n, orig, arr);
        QuickSort1(n, arr, &M1i[s], &C1i[s], &D1i[s]);

        FillDec(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2d[s], &C2d[s], &D2d[s]);

        FillRand(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2r[s], &C2r[s], &D2r[s]);

        FillInc(n, orig); CopyArray(n, orig, arr);
        QuickSort2(n, arr, &M2i[s], &C2i[s], &D2i[s]);
    }

    /* ===================================================================
     * Таблица 1: Трудоемкость метода Хоара (версия 1)
     * =================================================================== */
    printf("┌───────────────────────────────────────────────────────────┐\n");
    printf("│                Трудоемкость метода Хоара                  │\n");
    printf("├──────┬───────────────────────────────────────────────┤\n");
    printf("│      │            QuickSort1 (Мф+Сф)                  │\n");
    printf("│  N   ├─────────────┬─────────────┬──────────────┤\n");
    printf("│      │    Убыв.    │    Возр.    │     Случ.    │\n");
    printf("├──────┼─────────────┼─────────────┼──────────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %11d │ %11d │ %12d │\n",
            sizes[s],
            M1d[s] + C1d[s], M1i[s] + C1i[s], M1r[s] + C1r[s]);
    }
    printf("└──────┴─────────────┴─────────────┴──────────────┘\n");

    /* ===================================================================
     * Таблица 2: Глубина рекурсии
     * =================================================================== */
    printf("\n┌────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│            Глубина рекурсии сортировки методом Хоара                         │\n");
    printf("├──────┬───────────────────────────────────────────────────────────────────┤\n");
    printf("│      │          QuickSort1              │          QuickSort2              │\n");
    printf("│  n   ├──────────┬──────────┬────────────├──────────┬──────────┬────────────┤\n");
    printf("│      │  Убыв.   │  Случ.   │   Возр.    │  Убыв.   │  Случ.   │   Возр.    │\n");
    printf("├──────┼──────────┼──────────┼────────────┼──────────┼──────────┼────────────┤\n");
    for (int s = 0; s < 5; s++) {
        printf("│ %4d │ %8d │ %8d │ %10d │ %8d │ %8d │ %10d │\n",
            sizes[s],
            D1d[s], D1r[s], D1i[s],
            D2d[s], D2r[s], D2i[s]);
    }
    printf("└──────┴──────────┴──────────┴────────────┴──────────┴──────────┴────────────┘\n");

    /* ===================================================================
     * Таблица 3: QuickSort1 — теория vs факт, M и C раздельно
     *
     * Маппинг случаев для QS1 (опорный = средний элемент):
     *   Убыв. и Возр. → опорный ≈ медиана → ЛУЧШИЙ случай: M = n*log2(n), C = n*log2(n)
     *   Случ. → опорный непредсказуем → ХУДШИЙ/СРЕДНИЙ: M = 3(n-1), C = (n^2+5n+4)/2
     * =================================================================== */
    printf("\n┌─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│                         QuickSort1: теория vs факт (опорный = средний элемент)                           │\n");
    printf("├──────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┬──────────┤\n");
    printf("│  N   │Mt(убыв)  │Ct(убыв)  │Мф(убыв)  │Сф(убыв)  │Mt(случ)  │Ct(случ)  │Мф(случ)  │Сф(случ)  │Mt(возр)  │Ct(возр)  │Мф(возр)  │Сф(возр)  │\n");
    printf("├──────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┼──────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        /* Убыв./Возр. — лучший случай (QS1: опорный=средний ≈ медиана) */
        int Mt_best = (int)(n * lg);
        int Ct_best = (int)(n * lg);
        /* Случ. — худший/средний случай */
        int Mt_worst = 3 * (n - 1);
        int Ct_worst = (n * n + 5 * n + 4) / 2;
        printf("│ %4d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │ %8d │\n",
            n,
            Mt_best,  Ct_best,  M1d[s], C1d[s],
            Mt_worst, Ct_worst, M1r[s], C1r[s],
            Mt_best,  Ct_best,  M1i[s], C1i[s]);
    }
    printf("└──────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┴──────────┘\n");

    /* ===================================================================
     * Таблица 4: QuickSort1 — теоретические значения и отклонение факта (%)
     * =================================================================== */
    printf("\n┌──────────────────────────────────────────────────────────────────────────────────────────────────────────────┐\n");
    printf("│       QuickSort1: теоретические M и C + отклонение факта (%%)                                                    │\n");
    printf("├──────┬──────────┬──────────┬─────────────────────────┬──────────────────────────┬────────────────────────────┤\n");
    printf("│      │          │          │    Убывающий (лучший)   │     Случайный (худший)    │   Возрастающий (лучший)    │\n");
    printf("│  N   │  Mt теор │  Ct теор │   dM(убыв)    dC(убыв)   │    dM(случ)    dC(случ)    │  dM(возр)    dC(возр)      │\n");
    printf("├──────┼──────────┼──────────┼─────────────────────────┼──────────────────────────┼────────────────────────────┤\n");
    for (int s = 0; s < 5; s++) {
        int n = sizes[s];
        double lg = log2((double)n);
        int Mt_best  = (int)(n * lg);
        int Ct_best  = (int)(n * lg);
        int Mt_worst = 3 * (n - 1);
        int Ct_worst = (n * n + 5 * n + 4) / 2;
        /* убыв. → используем лучший случай как эталон */
        double dMd = 100.0 * (M1d[s] - Mt_best)  / Mt_best;
        double dCd = 100.0 * (C1d[s] - Ct_best)  / Ct_best;
        /* случ. → худший случай */
        double dMr = 100.0 * (M1r[s] - Mt_worst) / Mt_worst;
        double dCr = 100.0 * (C1r[s] - Ct_worst) / Ct_worst;
        /* возр. → лучший случай */
        double dMi = 100.0 * (M1i[s] - Mt_best)  / Mt_best;
        double dCi = 100.0 * (C1i[s] - Ct_best)  / Ct_best;
        printf("│ %4d │ %8d │ %8d │  %+7.1f%%   %+7.1f%%  │   %+7.1f%%    %+7.1f%%    │   %+7.1f%%    %+7.1f%%      │\n",
            n, Mt_best, Ct_best,
            dMd, dCd, dMr, dCr, dMi, dCi);
    }
    printf("└──────┴──────────┴──────────┴─────────────────────────┴──────────────────────────┴────────────────────────────┘\n");

    /* Выводы */
    printf("\nВывод: QuickSort1 на убыв./возр. массивах — лучший случай: M~C~n*log2(n).\n");
    printf("Вывод: QuickSort1 на случайных данных — худший/средний: M=3(n-1), C=(n^2+5n+4)/2.\n");
    printf("Вывод: QuickSort значительно быстрее квадратичных сортировок: O(n*log(n)) против O(n^2).\n");
    printf("Вывод: Версия 2 (медиана из трёх) даёт меньшую глубину рекурсии и более стабильную производительность.\n");

    return 0;
}
